#include "libftprintf_bonus.h"

int	ft_skip(const char *s)
{
	int	i;

	i = 0;
	while (s[i] != '\0' && ((s[i] >= '0' && s[i] <= '9') || s[i] == '+'))
		i++;
	return (i);
}
